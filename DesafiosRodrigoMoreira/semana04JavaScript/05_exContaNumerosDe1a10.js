//Utilizando FOR:
function contaNumeroFor(){
    for (let i = 1; i <= 10; i++){
        console.log(i);
    }
}

contaNumeroFor();

//Utilizando WHILE:
function contaNumeroWhile(){
    let numero = 1;
    while(numero <= 10){
        console.log(numero);
        numero++;
    }
}

contaNumeroWhile();